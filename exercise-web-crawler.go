package main

import (
	"fmt"
    "sync"
)

type SafeMap struct {
    mu sync.Mutex
    m map[string]int
}

func (visited SafeMap) Add(url string) {
    visited.mu.Lock()
    visited.m[url] = 1
    visited.mu.Unlock()
}

func (visited SafeMap) Get(url string) bool {
    visited.mu.Lock()
    defer visited.mu.Unlock()
    return visited.m[url] == 1
}

type Fetcher interface {
	// Fetch returns the body of URL and
	// a slice of URLs found on that page.
	Fetch(url string) (body string, urls []string, err error)
}

// Crawl uses fetcher to recursively crawl
// pages starting with url, to a maximum of depth.
func Crawl(visited SafeMap, url string, depth int, fetcher Fetcher) {
	if depth <= 0 {
		return
	}
	body, urls, err := fetcher.Fetch(url)
	if err != nil {
		fmt.Println(err)
		return
	}
	fmt.Printf("found: %s %q\n", url, body)
	for _, u := range urls {
        in := visited.Get(url)
        if !in {
            visited.Add(u)
		    go Crawl(visited, u, depth-1, fetcher)
        }
	}
	return
}

func main() {
    visited := SafeMap{m: make(map[string]int)}
	go Crawl(visited, "https://golang.org/", 4, fetcher)
}

